#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char *s1, char const *s2)
{
	char	*str;
	int		len;

	if (s2 == NULL)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2);
	if (!(str = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	ft_strcpy(str, s1);
	ft_strcat(str, s2);
	return (str);
}
